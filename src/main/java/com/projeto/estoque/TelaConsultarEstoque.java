    // Método que vai ler o banco de dados e colocar as linhas na tabela
    private void preencherTabela() {
        // 1. Pega o modelo da tabela que você desenhou no Design
        DefaultTableModel modelo = (DefaultTableModel) tabelaMateriais.getModel();
        
        // Limpa a tabela para não duplicar dados caso reabra
        modelo.setRowCount(0); 
        
        // 2. Busca todos os materiais salvos no banco H2
        List<Material> listaMateriais = materialRepository.findAll();
        
        // 3. Coloca cada material dentro da tabela do Swing
        for (Material m : listaMateriais) {
            modelo.addRow(new Object[]{
                m.getId(),
                m.getNome(),
                m.getTipo(),
                m.getQuantidade(),
                m.getPesoKg(),
                m.getPreco()
            });
        }
    }

