    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tabelaMateriais.getSelectedRow();
        
        if (linhaSelecionada == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecione um material na tabela para editar.");
            return;
        }
        
        Long idMaterial = (Long) tabelaMateriais.getValueAt(linhaSelecionada, 0);
        Material materialParaEditar = materialRepository.findById(idMaterial).orElse(null);
        
        if (materialParaEditar != null) {
            TelaCadastroMaterial cadastro = context.getBean(TelaCadastroMaterial.class);

            // Passa o material que encontramos para a tela de cadastro carregar nos campos
            cadastro.prepararParaEdicao(materialParaEditar);

            this.dispose(); // Fecha a tela de consulta
        
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        // 1. Verifica qual linha o usuário selecionou
        int linhaSelecionada = tabelaMateriais.getSelectedRow();
        
        if (linhaSelecionada == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecione um material na tabela para excluir.");
            return;
        }
        
        // 2. Pergunta se o usuário tem certeza (Evita exclusões por acidente)
        int confirmacao = javax.swing.JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir este material?", "Confirmar Exclusão", javax.swing.JOptionPane.YES_NO_OPTION);
        
        if (confirmacao == javax.swing.JOptionPane.YES_OPTION) {
            // 3. Pega o ID do material (Coluna 0 da tabela)
            Long idMaterial = (Long) tabelaMateriais.getValueAt(linhaSelecionada, 0);

            // 4. Deleta do banco H2 usando o Spring Data
            materialRepository.deleteById(idMaterial);

            // 5. Atualiza a tabela na tela para sumir com a linha apagada
            preencherTabela();
            
            javax.swing.JOptionPane.showMessageDialog(this, "Material excluído com sucesso!");
        }                
    }//GEN-LAST:event_btnExcluirActionPerformed

